function updateData(data) {
    data = JSON.parse(data);
    data[0].forEach((e, i) => {
        var tmp = e.up;
        data[0][i].up = e.dn;
        data[0][i].dn = tmp;
    });
    data[0].reverse();

    var map = document.getElementById("map");
    var src = '';
    data.forEach((e, i) => {
        e.forEach((e, j) => {
            var p = pos[i][j], size = 20;
            var x = p.x, y = p.y;
            y -= 20;
            if (p.rot) {
                size = 40;
            }

            if (e.up) {
                x += 10;
                var dir = 'up';
                if (p.rot) {
                    dir = p.rot[dir];
                    x -= 30;
                    if (dir == 'left') y -= 10;
                    if (dir == 'right') y += 30;
                }
                src += '<image xlink:href="images/' + dir + '.png" x="' + x + '" y="' + y + '" width="' + size + 'px" opacity="0.7" />';
            }

            x = pos[i][j].x;
            y = p.y - 20;
            if (e.dn) {
                x -= 30;
                var dir = 'down';
                if (p.rot) {
                    dir = p.rot[dir];
                    x += 10;
                    if (dir == 'left') y -= 10;
                    if (dir == 'right') y += 30;
                }
                src += '<image xlink:href="images/' + dir + '.png" x="' + x + '" y="' + y + '" width="' + size + 'px" opacity="0.7" />';
            }
        });
    });
    map.innerHTML = mapData + src;
}

